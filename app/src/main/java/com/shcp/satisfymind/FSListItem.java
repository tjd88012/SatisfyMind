package com.shcp.satisfymind;

public class FSListItem {

    int no;
    String title;
    String text;
    String nickname;
    int favor;

    public FSListItem() {
    }

    public FSListItem(int no, String title, String text, String nickname, int favor) {
        this.no = no;
        this.title = title;
        this.text = text;
        this.nickname = nickname;
        this.favor = favor;
    }
}
