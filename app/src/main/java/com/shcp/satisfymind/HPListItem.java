package com.shcp.satisfymind;

public class HPListItem {

    int no;
    String nickname;
    String file;
    String text;
    int favor;

    public HPListItem() {
    }

    public HPListItem(int no, String nickname, String file, String text, int favor) {
        this.no = no;
        this.nickname = nickname;
        this.file = file;
        this.text = text;
        this.favor = favor;
    }
}
